function topTracks(tracks) {
  let tracksObj = {}

  for (track in tracks.items) {
    let currentArtist = tracks.items[track].artists[0].name //current artist title
    let currentTrack = tracks.items[track].name //current track title
    let currentTrackObj = {}
    currentTrackObj[currentTrack] = track //this will create an object like: {'Love Me Do': 47}

    if (!tracksObj[currentArtist]) { //if artist not yet in list, create their hits array
      tracksObj[currentArtist] = []
      tracksObj[currentArtist].push(currentTrackObj)
    } else { //if they are already in the list, add to their hits array
      tracksObj[currentArtist].push(currentTrackObj)
    }

  }
  return tracksObj
}

